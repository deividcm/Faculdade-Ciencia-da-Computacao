    private void btnMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMedicoActionPerformed
        DlgEscolhaMedico escolha = new DlgEscolhaMedico(this.pai,true,this.pai.getGerenteMedicos());
        escolha.setLocation(this.getLocation());
        escolha.setVisible(true);
        
        Medico medico = escolha.getMedicoEscolhido();
        if(medico != null){
            this.consulta.setMedico(medico);
            this.atualizarConsulta();
        }
    }//GEN-LAST:event_btnMedicoActionPerformed

