    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(!this.edtCodigo.getText().isBlank() && !this.consulta.getMedico().getCrm().isBlank() && !this.consulta.getPaciente().getCpf().isBlank()){
            this.camposParaObjeto();
            this.pronto = true;
            this.dispose();
        }
        else if (this.edtCodigo.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Preencha o código!");
        }
        else if(this.consulta.getMedico().getCrm().isBlank()){
            JOptionPane.showMessageDialog(this, "Escolha um Médico!");
        }
        else{
            JOptionPane.showMessageDialog(this, "Escolha um Paciente!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPacienteActionPerformed
        DlgEscolhaPaciente escolha = new DlgEscolhaPaciente(this.pai,true,this.pai.getGerentePacientes());
        escolha.setLocation(this.getLocation());
        escolha.setVisible(true);
        
        Paciente paciente = escolha.getPacienteEscolhido();
        if(paciente != null){
            this.consulta.setPaciente(paciente);
            this.atualizarConsulta();
        }
    }//GEN-LAST:event_btnPacienteActionPerformed

    public void camposParaObjeto(){
        this.consulta.setCod(this.edtCodigo.getText());
        this.consulta.setData(this.edtData.getText());
        this.consulta.setHora(this.edtHora.getText());
    }
    
    public void objetoParaCampos(){
        this.edtCodigo.setText(this.consulta.getCod());
        this.edtData.setText(this.consulta.getData());
        this.edtHora.setText(this.consulta.getHora());
    }
    
    public boolean isPronto() {
        return this.pronto;
    }

    public void atualizarConsulta(){
        this.edtDisciplina.setText(this.consulta.getMedico().toString() + this.consulta.getPaciente().toString());
    }
    

