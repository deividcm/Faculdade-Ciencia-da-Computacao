AND V.NOME_VENDEDOR = 'Joao' AND (C.CIDADE = 'Rio de Janeiro' OR C.CIDADE = 'Niterói');
/*34*/
SELECT DISTINCT P.DESCRICAO FROM PRODUTO P, ITEM_PEDIDO IP
