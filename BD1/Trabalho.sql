SELECT PE.ID_PEDIDO, COUNT(IP.ID_PRODUTO) AS QUANTIDADE_PRODUTO FROM PEDIDO PE, ITEM_PEDIDO IP
WHERE PE.ID_PEDIDO = IP.ID_PEDIDO
GROUP BY PE.ID_PEDIDO;
/*25*/
SELECT PE.ID_PEDIDO, COUNT(ID_PRODUTO) AS QUANTIDADE_PRODUTO FROM PEDIDO PE, ITEM_PEDIDO IP
WHERE PE.ID_PEDIDO = IP.ID_PEDIDO
GROUP BY PE.ID_PEDIDO
HAVING COUNT(ID_PRODUTO) > 3;
/*26*/
