ORDER BY PE.PRAZO_ENTREGA DESC;
/*29*/
SELECT V.NOME_VENDEDOR FROM VENDEDOR V, PEDIDO PE
WHERE V.ID_VENDEDOR = PE.ID_VENDEDOR AND
PE.PRAZO_ENTREGA > 15 AND V.SALARIO_FIXO >= 1000.00
ORDER BY V.NOME_VENDEDOR ASC;
/*30*/
SELECT C.NOME_CLIENTE FROM CLIENTE C, PEDIDO PE, ITEM_PEDIDO IP, PRODUTO P
WHERE C.ID_CLIENTE = PE.ID_CLIENTE AND
PE.ID_PEDIDO = IP.ID_PEDIDO AND
P.ID_PRODUTO = IP. ID_PRODUTO AND
P.DESCRICAO = 'QUEIJO' AND C.UF = 'RJ'
ORDER BY C.NOME_CLIENTE ASC;
/*31*/
SELECT V.NOME_VENDEDOR FROM VENDEDOR V, PEDIDO PE, ITEM_PEDIDO IP, PRODUTO P
WHERE V.ID_VENDEDOR = PE.ID_VENDEDOR AND
PE.ID_PEDIDO = IP.ID_PEDIDO AND
P.ID_PRODUTO = IP.ID_PRODUTO AND
P.DESCRICAO = 'CHOCOLATE' AND P.UNIDADE = 'KG' AND IP.QUANTIDADE > 10
ORDER BY V.NOME_VENDEDOR ASC;
/*32*/
SELECT DISTINCT COUNT(ID_CLIENTE) FROM PEDIDO PE, VENDEDOR V
