CREATE TABLE CLIENTE (COD_CLIENTE INTEGER PRIMARY KEY,
					  NOME_CLIENTE VARCHAR(40) NOT NULL,
					  CID_CLIENTE VARCHAR(20),
					  CEP_CLIENTE NUMERIC(8),
					  UF_CLIENTE VARCHAR(2),
					  CNPJ_CLIENTE NUMERIC(14),
					  INSCR_ESTADUAL_CLIENTE NUMERIC(14));

CREATE TABLE VENDEDOR (COD_VENDEDOR INTEGER PRIMARY KEY,
					   NOME_VENDEDOR VARCHAR(40) NOT NULL,
					   SALARIO_FIXO NUMERIC(6,2) NOT NULL,
					   FAIXA_DE_COMISSAO NUMERIC(2,2) NOT NULL);

CREATE TABLE PEDIDO (NUM_PEDIDO INTEGER PRIMARY KEY,
					 PRAZO_ENTREGA_PEDIDO NUMERIC(4) NOT NULL,
					 COD_CLIENTE INTEGER,
					 COD_VENDEDOR INTEGER);

CREATE TABLE PRODUTO (NUM_PRODUTO INTEGER PRIMARY KEY,
					  UNIDADE_PRODUTO VARCHAR(10) NOT NULL,
					  DESCRICAO_PRODUTO VARCHAR(100) NOT NULL,
					  VALOR_UNITARIO NUMERIC(6,2) NOT NULL);

CREATE TABLE ITEM_PEDIDO (NUM_PEDIDO INTEGER,
						  NUM_PRODUTO INTEGER,
						  QUANT_PEDIDA INTEGER NOT NULL);

